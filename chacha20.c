#define CC20_ROT(a,b) (((a) << (b)) | ((a) >> (32 - (b))))
#define CC20_QR(a, b, c, d) \
  a += b; d ^= a; d = CC20_ROT(d, 16); \
  c += d; b ^= c; b = CC20_ROT(b, 12); \
  a += b; d ^= a; d = CC20_ROT(d, 8); \
  c += d; b ^= c; b = CC20_ROT(b, 7); \

void chacha20_block(uint32_t *state,
                    const void *key,
                    uint64_t nonce,
                    uint64_t counter)
{
  uint32_t init[16] = {
    0x61707865, 0x3320646e, 0x79622d32, 0x6b206574,
    ((uint32_t*)key)[0], ((uint32_t*)key)[1],
    ((uint32_t*)key)[2], ((uint32_t*)key)[3],
    ((uint32_t*)key)[4], ((uint32_t*)key)[5],
    ((uint32_t*)key)[6], ((uint32_t*)key)[7],
    ((uint32_t*)&counter)[0], ((uint32_t*)&counter)[1],
    ((uint32_t*)&nonce)[0], ((uint32_t*)&nonce)[1]
  };

  for (int i = 0; i < 16; ++i) {
    state[i] = init[i];
  }

  for (int i = 0; i < 20; i += 2) {
    CC20_QR(state[0], state[4], state[8], state[12]);
    CC20_QR(state[1], state[5], state[9], state[13]);
    CC20_QR(state[2], state[6], state[10], state[14]);
    CC20_QR(state[3], state[7], state[11], state[15]);

    CC20_QR(state[0], state[5], state[10], state[15]);
    CC20_QR(state[1], state[6], state[11], state[12]);
    CC20_QR(state[2], state[7], state[8], state[13]);
    CC20_QR(state[3], state[4], state[9], state[14]);
  }

  for (int i = 0; i < 16; ++i) {
    state[i] += init[i];
  }
}

void chacha20(
  const void *key, uint64_t nonce,
  void *output, const void *input,
  size_t len)
{
  uint32_t block[16];
  for (size_t i = 0; i < len; i += 64) {
    chacha20_block(block, key, nonce, i/64);
    for (size_t j = 0; j < 64 && i + j < len; ++j) {
      ((uint8_t*)output)[i+j] = ((uint8_t*)input)[i+j] ^ ((uint8_t*)block)[j];
    }
  }
}

